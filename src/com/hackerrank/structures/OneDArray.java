package com.hackerrank.structures;

import java.util.Scanner;

public class OneDArray {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int size = sc.nextInt();
		int[] numbers = new int[size];
		int index = 0;
		while ( sc.hasNext() ){
			numbers[index] = sc.nextInt();
			index++;
		}
		
		//int
	}

}
